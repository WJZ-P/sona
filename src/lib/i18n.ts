const dictionaries = {
  'zh-CN': zhCN,
  'en-US': enUS,
} as const

export const languageOptions = [
  { value: 'zh-CN', label: '简体中文' },
  { value: 'en-US', label: 'English' },
] as const

function getByPath(source: typeof zhCN, key: string): string | undefined {
  const value = key.split('.').reduce<unknown>((current, part) => {
    if (current && typeof current === 'object' && part in current) {
      return (current as Record<string, unknown>)[part]
    }
    return undefined
  }, source)

  return typeof value === 'string' ? value : undefined
}

function interpolate(template: string, params?: Params): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`))
}

export function getLocale(): Locale {
  return store.get('language')
}

export function setLocale(locale: Locale) {
  store.set('language', locale)
}

export function translate(key: string, params?: Params, locale = getLocale()): string {
  const dictionary = dictionaries[locale] ?? dictionaries['zh-CN']
  const fallback = dictionaries['zh-CN']
  return interpolate(getByPath(dictionary, key) ?? getByPath(fallback, key) ?? key, params)
}

export function useI18n() {
  const [locale, setLocaleState] = useState<Locale>(() => getLocale())

  useEffect(() => store.onChange('language', setLocaleState), [])

  return {
    locale,
    setLocale,
    t: (key: string, params?: Params) => translate(key, params, locale),
  }
}
